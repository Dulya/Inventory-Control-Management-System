    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        
        int res;
        try {
            String password = new String(passwordText.getPassword());
            String confirmPassword = new String(confirmText.getPassword());
            int privilrge=Integer.parseInt(priviledgeButton.getText());
            if(privilrge==1){
                 if (password.equals(confirmPassword)) {
                res = UserController.setUser(nameText.getText(), new String(passwordText.getPassword()), Integer.parseInt(priviledgeButton.getText()));
                if (res > 0) {
                    createNewUser=true;
                    JOptionPane.showMessageDialog(null, "new User has Created!");
                    dispose();
                } else {
                    createNewUser=false;
                    JOptionPane.showMessageDialog(null, "Failed to create new User!!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password does not matches! please reconfirm Password!");
            }
            }else{
               // JOptionPane.showMessageDialog("", res);
            }
           
        } catch (SQLException ex) {
            Logger.getLogger(UserProfileForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UserProfileForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_createButtonActionPerformed

