    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (!(idText.getText().isEmpty() || nameText.getText().isEmpty())) {
            int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to add Publisher " + nameText.getText() + " ? ", "Confirm", JOptionPane.YES_NO_OPTION);
            if (response == 0) {
                Publisher publisher = new Publisher(idText.getText(), nameText.getText());
                try {
                    int res = publisherController.addPublisher(publisher);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Publisher added successfully.");
                        idText.requestFocus();
                        nextId = IdGenerator.getNextId("Publisher", "idPublisher", "P");
                        idText.setText(nextId);
                        nameText.setText("");
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to add Publisher.");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(AddPublisherForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(AddPublisherForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                idText.requestFocus();
                idText.setEnabled(true);
                idText.setText("");
                nameText.setText("");
            }
        } else if (idText.getText().isEmpty() && nameText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fields Are Empty.Please fill them to add new Publisher!");
            idText.requestFocus();
        }
    }//GEN-LAST:event_addButtonActionPerformed
