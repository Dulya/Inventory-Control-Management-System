    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        idText.setText("");
        if (!nameText.getText().isEmpty()) {
            Category category1 = (Category) idCombo.getSelectedItem();
            int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete Category " + category1.getCat_Name() + " ? ", "Confirm", JOptionPane.YES_NO_OPTION);
            if (response == 0) {
                int res;
                try {
                    res = CategoryController.deleteCategory(category1.getCategoryId());
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Deleted Successfully.");
                        idText.setEnabled(true);
                        idText.requestFocus();
                        nextId = IdGenerator.getNextId("Category", "idCategory", "CA");
                        idText.setText(nextId);
                        nameText.setText("");
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to delete.");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(ManageCategoryForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ManageCategoryForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                idText.requestFocus();
                idText.setEnabled(true);
                idText.setText("");
                nameText.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Category from Category Box to delete.");
            idCombo.requestFocus();
            idText.requestFocus();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (!(idText.getText().isEmpty() || nameText.getText().isEmpty())) {
            int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to add Category " + nameText.getText() + " ? ", "Confirm", JOptionPane.YES_NO_OPTION);
            if (response == 0) {
                Category category = new Category(idText.getText(), nameText.getText());
                try {
                    int res = CategoryController.addCategory(category);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Category added successfully.");
                        idText.requestFocus();
                        nextId = IdGenerator.getNextId("Category", "idCategory", "CA");
                        idText.setText(nextId);
                        nameText.setText("");
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to add Category.");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(ManageCategoryForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ManageCategoryForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                idText.requestFocus();
                idText.setEnabled(true);
                idText.setText("");
                nameText.setText("");
            }
        } else if (idText.getText().isEmpty() && nameText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fields Are Empty.Please fill them to add new Category!");
            idText.requestFocus();
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed

        idText.setText("");
        if (!(nameText.getText().isEmpty())) {
            Category category1 = (Category) idCombo.getSelectedItem();
            int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to update Category " + category1.getCat_Name() + " ? ", "Confirm", JOptionPane.YES_NO_OPTION);
            if (response == 0) {
                Category category = new Category(category1.getCategoryId(), nameText.getText());
                int res;
                try {
                    res = CategoryController.updateCategory(category);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Updated Successfully.");
                        idText.requestFocus();
                        idText.setEnabled(true);
                        nextId = IdGenerator.getNextId("Category", "idCategory", "CA");
                        idText.setText(nextId);
                        nameText.setText("");
                        idText.requestFocus();
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to Update.");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(ManageCategoryForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ManageCategoryForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                idText.requestFocus();
                idText.setEnabled(true);
                idText.requestFocus();
                idText.setText("");
                nameText.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Category from Publisher Box to Update .");
            idCombo.requestFocus();
            idText.requestFocus();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

