    private void fillIdCombo() throws SQLException, ClassNotFoundException {
        List<Category> categoryList = CategoryController.getAllCategory();
        b = false;
        idCombo.removeAllItems();
        for (Category category : categoryList) {
            idCombo.addItem(category);
        }
        b = true;
    }

    public void check() {
        boolean bool = idText.getText().isEmpty() || nameText.getText().isEmpty();
        if (bool != true) {
            addButton.setEnabled(true);
        } else {
            addButton.setEnabled(false);
        }
    }
