    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to add Item?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (response == 0) {
            String text = descriptionText.getText().toUpperCase();
            if (text.equals("BOOK")) {
                Category category = (Category) categoryCombo.getSelectedItem();
                Author author = (Author) authorCombo.getSelectedItem();
                Language language = (Language) languageCombo.getSelectedItem();
                Publisher publisher = (Publisher) publisherCombo.getSelectedItem();
                Supplier supplier = (Supplier) supCombo.getSelectedItem();
                Item item = new Item(itemText.getText(), descriptionText.getText(), Double.parseDouble(costText.getText()), Double.parseDouble(salesText.getText()), Double.parseDouble(discountText.getText()), Integer.parseInt(qtyText.getText()), Integer.parseInt(reOrderText.getText()), supplier.getSupplierId());
                Book book = new Book(titleText.getText(), publisher.getPublisherId(), author.getAuthorId(), language.getLangId(), bookIdText.getText(), itemText.getText(), category.getCategoryId());
                int res;
                int res1;
                try {
                    res = ItemController.addItem(item);
                    res1 = BookController.addBook(book);
                    if (res > 0 && res1 > 0) {
                        JOptionPane.showMessageDialog(null, "Item added successfully!");
                        String nextId = IdGenerator.getNextId("Item", "idItem", "I");
                        itemText.setText(nextId);
                        descriptionText.setText("");
                        discountText.setText("");
                        titleText.setText("");
                        salesText.setText("");
                        costText.setText("");
                        qtyText.setText("");
                        reOrderText.setText("");
                        bookIdText.setText("");
                        descriptionText.requestFocus();
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to Add Item.");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(AddItemForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(AddItemForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                Supplier supplier = (Supplier) supCombo.getSelectedItem();
                Item item = new Item(itemText.getText(), descriptionText.getText(), Double.parseDouble(costText.getText()), Double.parseDouble(salesText.getText()), Double.parseDouble(discountText.getText()), Integer.parseInt(qtyText.getText()), Integer.parseInt(reOrderText.getText()), supplier.getSupplierId());
                try {
                    int res = ItemController.addItem(item);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Item added successfully!");
                        String nextId = IdGenerator.getNextId("Item", "idItem", "I");
                        itemText.setText(nextId);
                        descriptionText.setText("");
                        discountText.setText("");
                        titleText.setText("");
                        salesText.setText("");
                        costText.setText("");
                        qtyText.setText("");
                        reOrderText.setText("");
                        bookIdText.setText("");
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to Add Item.");
                    }
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_registerButtonActionPerformed

