    public void check() {
        String desName = descriptionText.getText().toUpperCase();
        if (!desName.equals("BOOK")) {
            boolean b = discountText.getText().isEmpty() || costText.getText().isEmpty() || salesText.getText().isEmpty() || reOrderText.getText().isEmpty() || qtyText.getText().isEmpty();
            if (b == false) {
                registerButton.setEnabled(true);
            } else {
                registerButton.setEnabled(false);
            }
        } else {
            boolean b = discountText.getText().isEmpty() || costText.getText().isEmpty() || salesText.getText().isEmpty() || reOrderText.getText().isEmpty() || qtyText.getText().isEmpty() || bookIdText.getText().isEmpty() || titleText.getText().isEmpty();
            if (b == false) {
                registerButton.setEnabled(true);
            } else {
                registerButton.setEnabled(false);
            }
        }
    }
