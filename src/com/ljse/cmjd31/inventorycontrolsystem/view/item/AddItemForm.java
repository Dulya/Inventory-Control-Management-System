    private void descriptionTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_descriptionTextKeyReleased

        String text = descriptionText.getText().toUpperCase();
        if (text.equals("BOOK")) {
            String nextId1;
            try {
                nextId1 = IdGenerator.getNextId("Book", "bookId", "B");
                bookIdText.setText(nextId1);
                bookIdText.setEnabled(true);
                titleText.setEnabled(true);
                titleText.setBackground(Color.WHITE);
                bookIdText.setBackground(Color.WHITE);
                languageCombo.setEnabled(true);
                authorCombo.setEnabled(true);
                categoryCombo.setEnabled(true);
                publisherCombo.setEnabled(true);
                jPanel3.setBackground(Color.WHITE);
                categoryButton.setEnabled(true);
                authorButton.setEnabled(true);
                languageButton.setEnabled(true);
                publisherButton.setEnabled(true);
            } catch (SQLException ex) {
                Logger.getLogger(AddItemForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddItemForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            bookIdText.setEnabled(false);
            titleText.setEnabled(false);
            titleText.setText("");
            titleText.setBackground(Color.LIGHT_GRAY);
            bookIdText.setText("");
            bookIdText.setBackground(Color.LIGHT_GRAY);
            languageCombo.setEnabled(false);
            authorCombo.setEnabled(false);
            categoryCombo.setEnabled(false);
            publisherCombo.setEnabled(false);
            categoryButton.setEnabled(false);
            authorButton.setEnabled(false);
            languageButton.setEnabled(false);
            publisherButton.setEnabled(false);
            jPanel3.setBackground(Color.LIGHT_GRAY);
        }
    }//GEN-LAST:event_descriptionTextKeyReleased

